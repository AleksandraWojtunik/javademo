public class TypyDanych {
    public static void main(String[] args) {
        System.out.println(10);  //liczby całkowite
        System.out.println(10.5);  //liczby zmiennoprzecinkowe zapisujemy z kropką
        System.out.println("jakiś tekst w javie");  //tekst
        System.out.println('a');  //pojedyńczy znak - może być litera lub cyfra
        System.out.println(true);  //prawda lub fałsz
        System.out.println(false);
    }
}
