public class OperatoryPorownania {

    public static void main(String[] args) {

        int numOne = 1;
        int numTwo = 2;

        System.out.println(numOne > numTwo);
        System.out.println(numOne < numTwo);
        System.out.println(numOne == numTwo); // gdy chcemy sprawdzić czy coś jest czemuś równe używamy ==
        System.out.println(numOne != numTwo); // gdy chcemy sprawdzić czy coś nie jest czemuś równe używamy !=




    }
}
