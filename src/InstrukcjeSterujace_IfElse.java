public class InstrukcjeSterujace_IfElse {

    public static void main(String[] args) {

        int numOne = 1;
        int numTwo = 2;

        if(numOne < numTwo){
            System.out.println("number one is less than number two");
        } else {
            System.out.println("number one is greater than number two");
        }
    }
}
